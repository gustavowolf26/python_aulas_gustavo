#Inicio

#String -> palavras 'utiliza-se aspas simples ou duplas'
marca = 'apple'
print('a variavel marca é do tipo: ', type(marca))

#Integer(inteiro) -> Números inteiros, exemplo: 1; 2; 3; 4...
idade = 21
print('a variavel idade é do tipo: ', type(idade)) 

#float(flutuante) -> números fracionados 
fração = 10/5
print('a variavel fração é do tipo: ', type(fração), fração)

#Bool(Booleano) -> True(verdadeiro) ou False(Falso)
fumante = True 
print('a variavel fumante é do tipo: ', type(fumante))

